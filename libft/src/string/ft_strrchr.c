char	*ft_strrchr(char *s, int c)
{
	int	i;
	int	x;

	i = 0;
	x = -1;
	while (s[i])
	{
		if (s[i] == (char)c)
			x = i;
		i++;
	}
	if (c == '\0')
		return ((char *)&s[i]);
	if (x == -1)
		return (0);
	return ((char *)&s[x]);
}
